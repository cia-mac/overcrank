def main():
    with open(PH_V2) as f:
        v2 = json.load(f)

    cameras = []
    updated = 0
    for cam in v2["cameras"]:
        model = cam.get("model_name", cam.get("model", "?"))
        cam_copy = dict(cam)

        if model in AGENT2_MODES and len(AGENT2_MODES[model]) > 0:
            new_modes = AGENT2_MODES[model]
            if len(new_modes) > len(cam_copy.get("modes", [])):
                old_count = len(cam_copy.get("modes", []))
                cam_copy["modes"] = new_modes
                cam_copy["data_quality"] = "verified"
                updated += 1
                print(f"  {model}: {old_count} -> {len(new_modes)} modes")

        cameras.append(cam_copy)

    total_modes = sum(len(c["modes"]) for c in cameras)
    output = dict(v2)
    output["cameras"] = cameras
    output["verified_date"] = "2026-04-13"
    output["notes"] = f"v3: Full resolution tables for Nova S6/S9/S12/S16 (20 modes each), Nova R2 (6 modes partial), Nova R3-4K/R5-4K (26 each), Mini WX50/WX100 (15 each), Mini UX50/UX100 (14-15 each), Mini R3-4K (40 modes), Mini R5-4K (43 modes) from official Photron datasheets and hardware manuals. Total: {len(cameras)} cameras, {total_modes} modes."

    with open(PH_V3, "w") as f:
        json.dump(output, f, indent=2)

    print(f"\nWrote photron_v3.json: {len(cameras)} cameras, {total_modes} modes ({updated} updated)")


if __name__ == "__main__":
    main()
